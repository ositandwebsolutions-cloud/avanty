document.addEventListener("DOMContentLoaded", () => {
    // Page Transition Overlay
    const overlay = document.getElementById("page-transition-overlay");
    setTimeout(() => { overlay.classList.add("hidden"); }, 50);
    
    document.querySelectorAll('.transition-link').forEach(link => {
        link.addEventListener('click', function(e) {
            if (e.ctrlKey || e.metaKey || this.target === "_blank") return;
            const href = this.getAttribute('href');
            if(href && href !== '#' && !href.startsWith('#')) {
                e.preventDefault();
                overlay.classList.remove("hidden");
                setTimeout(() => { window.location.href = href; }, 400); 
            }
        });
    });

    // Mobile Menu Toggle
    const menuToggle = document.getElementById('mobile-menu');
    const navLinks = document.getElementById('nav-links');

    if (menuToggle && navLinks) {
        menuToggle.addEventListener('click', () => {
            navLinks.classList.toggle('nav-active');
            menuToggle.classList.toggle('toggle-active');
        });
    }

    // Sticky Header Scroll Effect
    const header = document.querySelector('.header');
    if (header) {
        window.addEventListener('scroll', () => {
            if (window.scrollY > 50) {
                header.classList.add('scrolled');
            } else {
                header.classList.remove('scrolled');
            }
        });
    }
});

// --- ADD THIS TO FIX THE BACK BUTTON BLANK SCREEN ---
window.addEventListener('pageshow', (event) => {
    // event.persisted is true if the page was restored from the browser's BFCache
    if (event.persisted) {
        const overlay = document.getElementById("page-transition-overlay");
        if (overlay) {
            // Instantly hide the overlay when coming back to the page
            overlay.classList.add("hidden");
        }
    }
});

document.addEventListener("DOMContentLoaded", function() {
    const seeMoreBtn = document.querySelector('.see-more-btn');
    const disclaimerText = document.querySelector('.disclaimer-text');

    if (seeMoreBtn && disclaimerText) {
        seeMoreBtn.addEventListener('click', function() {
            // Toggle the expanded class
            disclaimerText.classList.toggle('expanded');
            
            // Update button text and aria-expanded state based on the class
            if (disclaimerText.classList.contains('expanded')) {
                seeMoreBtn.textContent = 'See less';
                seeMoreBtn.setAttribute('aria-expanded', 'true');
            } else {
                seeMoreBtn.textContent = 'See more';
                seeMoreBtn.setAttribute('aria-expanded', 'false');
            }
        });
    }
});



document.addEventListener("DOMContentLoaded", () => {
    const contactForm = document.getElementById('main-contact-form');
    const formFeedback = document.getElementById('form-feedback');
    const submitBtn = document.getElementById('submit-btn');

    if (contactForm) {
        contactForm.addEventListener('submit', async function(e) {
            e.preventDefault();
            
            submitBtn.innerHTML = 'SENDING...';
            submitBtn.disabled = true;

            const formData = new FormData(this);
            
            // 1. Grab the CSRF token generated by the @csrf blade directive
            const csrfToken = document.querySelector('input[name="_token"]')?.value;

            try {
                // 2. Fetch the URL dynamically, or hardcode the path if not using form action
                // Using the exact path since Blade doesn't work in external JS files
                const response = await fetch("/contact/submit", {
                    method: 'POST',
                    headers: {
                        'X-Requested-With': 'XMLHttpRequest',
                        'X-CSRF-TOKEN': csrfToken, // Explicitly pass the token
                        'Accept': 'application/json' // Tell Laravel we want JSON errors back
                    },
                    body: formData
                });

                const result = await response.json();

                if (response.ok) {
                    // Success
                    formFeedback.style.display = 'block';
                    formFeedback.style.color = '#4caf50'; 
                    formFeedback.innerText = result.message || 'Message sent successfully!';
                    contactForm.reset();
                } else if (response.status === 422) {
                    // Validation Errors from Laravel
                    formFeedback.style.display = 'block';
                    formFeedback.style.color = '#f44336'; 
                    
                    // Extract the first validation error message to show the user
                    const firstErrorKey = Object.keys(result.errors)[0];
                    formFeedback.innerText = result.errors[firstErrorKey][0];
                } else {
                    // Server Errors (500)
                    throw new Error(result.message || 'Server error occurred');
                }
            } catch (error) {
                // Network errors or caught exceptions
                console.error("Submission Error:", error);
                formFeedback.style.display = 'block';
                formFeedback.style.color = '#f44336'; 
                formFeedback.innerText = 'There was an error sending your message. Please check your connection and try again.';
            } finally {
                submitBtn.innerHTML = 'SEND MESSAGE <span>&rarr;</span>';
                submitBtn.disabled = false;
            }
        });
    }
});